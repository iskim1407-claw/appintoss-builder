// 마크다운을 간단한 HTML로 변환
export function markdownToHtml(markdown: string): string {
  let html = markdown
    // 제목
    .replace(/^### (.*$)/gim, '<h3 class="text-lg font-semibold mt-6 mb-2">$1</h3>')
    .replace(/^## (.*$)/gim, '<h2 class="text-xl font-bold mt-8 mb-3 text-gray-800">$1</h2>')
    .replace(/^# (.*$)/gim, '<h1 class="text-2xl font-bold mb-4 text-[#3182F6]">$1</h1>')
    // 굵게
    .replace(/\*\*(.*?)\*\*/gim, '<strong>$1</strong>')
    // 테이블 (간단한 처리)
    .replace(/\| (.*?) \|/gim, (match) => {
      const cells = match.split('|').filter(c => c.trim());
      const row = cells.map(cell => `<td class="border px-3 py-2 text-sm">${cell.trim()}</td>`).join('');
      return `<tr>${row}</tr>`;
    })
    .replace(/\|[-\s|]+\|/gim, '')
    // 리스트
    .replace(/^- (.*$)/gim, '<li class="ml-4 text-gray-700">$1</li>')
    .replace(/^(\d+)\. (.*$)/gim, '<li class="ml-4 text-gray-700"><span class="font-medium">$1.</span> $2</li>')
    // 단락
    .replace(/\n\n/gim, '</p><p class="my-3 text-gray-700 leading-relaxed">')
    // 수평선
    .replace(/^---$/gim, '<hr class="my-6 border-gray-200">')
    // 줄바꿈
    .replace(/\n/gim, '<br>');

  // 테이블 래핑 (연속된 tr 태그를 table로 감싸기)
  html = html.replace(/(<tr>[^]*?<\/tr>)+/g, (match) => {
    return `<table class="w-full border-collapse border border-gray-200 my-4">${match}</table>`;
  });

  return `<div class="prose max-w-none"><p class="my-3 text-gray-700 leading-relaxed">${html}</p></div>`;
}

